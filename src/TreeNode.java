public class TreeNode
{
  /*------------------------------------------------------------------------------------------------------------------*/

  int value;
  TreeNode left;
  TreeNode right;

  /*------------------------------------------------------------------------------------------------------------------*/

  public TreeNode (int value)
  {
    this.value = value;
    this.left = null;
    this.right = null;
  }

  /*------------------------------------------------------------------------------------------------------------------*/

  public void discard ()
  {
    this.value = 0;
    this.left = null;
    this.right = null;
  }

  /*------------------------------------------------------------------------------------------------------------------*/

  @Override
  public String toString ()
  {
    String left = this.left == null ? "null" : String.valueOf(this.left.value);
    String right = this.right == null ? "null" : String.valueOf(this.right.value);
    return ("[" + this.value + " -> (" + left + " : " + right + ")] ");
  }
}

